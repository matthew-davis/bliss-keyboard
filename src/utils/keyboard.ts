// Utils
export const getHomeMenu = (): number[][] => ([menuMap[1000]]);

export const getSpecialMenu = (): number[][] => [menuMap[2000]];

export const getIndicatorMenu = (): number[][] => [menuMap[3000]];

export const getNextMenu = (id: number, characterMap: number[][]): number[][] => {
  if (menuMap[id] === undefined || menuMap[id] === characterMap[characterMap.length - 1]) {
    return characterMap.concat(getIndicatorMenu());
  }

  return characterMap.concat([menuMap[id]]);
};

export const getPreviousMenu = (characterMap: number[][]): number[][] => {
  characterMap.pop();
  return [...characterMap];
};

